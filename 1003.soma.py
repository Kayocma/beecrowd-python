A = int(input()) # Recebe um valor inteiro e armazena na variável A
B = int(input()) # Recebe um valor inteiro e armazena na variável B

SOMA = A + B # Soma os valores de A e B e armazena na variável SOMA

print("SOMA = %d" % SOMA) # Exibe o valor de SOMA na tela. O % SOMA é a variável que será substituída pelo valor de SOMA. O %d é a formatação do número inteiro. O print exibe o valor de SOMA na tela.