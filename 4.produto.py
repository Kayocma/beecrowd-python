A = int(input()) # Recebe um valor inteiro e armazena na variável A
B = int(input()) # Recebe um valor inteiro e armazena na variável B

PROD = A * B # Multiplica os valores de A e B e armazena na variável PROD

print("PROD = %d" % PROD) # Exibe o valor de PROD na tela. O % PROD é a variável que será substituída pelo valor de PROD. O %d é a formatação do número inteiro. O print exibe o valor de PROD na tela.