A = float(input()) # Recebe um valor flutuante e armazena na variável A
B = float(input()) # Recebe um valor flutuante e armazena na variável B

MEDIA = (A * 3.5 + B * 7.5) / 11 # Calcula a média ponderada e armazena na variável MEDIA. A variável A tem peso 3.5 e a variável B tem peso 7.5.

print("MEDIA = %.5f" % MEDIA) # Exibe o valor de MEDIA na tela com 5 casas decimais. O % MEDIA é a variável que será substituída pelo valor de MEDIA. O %.5f é a formatação do número flutuante. O print exibe o valor de MEDIA na tela.