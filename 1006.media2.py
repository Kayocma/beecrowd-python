A = float(input()) # Recebe um valor flutuante e armazena na variável A
B = float(input()) # Recebe um valor flutuante e armazena na variável B
C = float(input()) # Recebe um valor flutuante e armazena na variável C

MEDIA = (A * 2 + B * 3 + C * 5) / 10 # Calcula a média ponderada e armazena na variável MEDIA. A variável A tem peso 2, a variável B tem peso 3 e a variável C tem peso 5.

print("MEDIA = %.1f" % MEDIA) # Exibe o valor de MEDIA na tela com 1 casa decimal. O % MEDIA é a variável que será substituída pelo valor de MEDIA. O %.1f é a formatação do número flutuante. O print exibe o valor de MEDIA na tela.