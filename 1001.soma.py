A = int(input()) # Recebe um valor inteiro e armazena na variável A
B = int(input()) # Recebe um valor inteiro e armazena na variável B

X = A + B # Soma os valores de A e B e armazena na variável X

print (f"X = {X}")  # O "f" antes das aspas duplas permite a interpolação de strings, ou seja, a substituição de variáveis dentro da string. O {X} é a variável que será substituída pelo valor de X. O print exibe o valor de X na tela.