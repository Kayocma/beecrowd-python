A = int(input()) # Recebe um valor inteiro e armazena na variável A
B = int(input()) # Recebe um valor inteiro e armazena na variável B
C = int(input()) # Recebe um valor inteiro e armazena na variável C
D = int(input()) # Recebe um valor inteiro e armazena na variável D

DIFERENCA = (A * B - C * D) # Calcula a diferença entre os valores de A e B e os valores de C e D e armazena na variável DIFERENCA

print("DIFERENCA = %d" % DIFERENCA) # Exibe o valor de DIFERENCA na tela. O % DIFERENCA é a variável que será substituída pelo valor de DIFERENCA. O %d é a formatação do número inteiro. O print exibe o valor de DIFERENCA na tela.