n = 3.14159 # pi
r = float(input()) # Recebe um valor flutuante e armazena na variável r
A = n * r**2 # Calcula a área do círculo e armazena na variável A 

print("A=%.4f" % A) # Exibe o valor de A na tela com 4 casas decimais. O % A é a variável que será substituída pelo valor de A. O %.4f é a formatação do número flutuante. O print exibe o valor de A na tela.